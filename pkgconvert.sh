build_rpm() {
  need rpmbuild rpm-build
  local top="$WORKDIR/rpmbuild"
  mkdir -p "$top"/{BUILD,RPMS,SPECS,SOURCES,BUILDROOT}
  local rname; rname="$(echo "$PKGNAME" | tr ' _' '--')"
  local spec="$top/SPECS/$rname.spec"
  # Desktop integration scriptlets, only when launchers/icons are present
  local POSTSECTION=""
  if find "$STAGE" \( -name '*.desktop' -o -path '*icons*' \) -print -quit 2>/dev/null | grep -q .; then
    POSTSECTION='
%post
command -v update-desktop-database >/dev/null 2>&1 && update-desktop-database -q /usr/share/applications || :
command -v gtk-update-icon-cache >/dev/null 2>&1 && gtk-update-icon-cache -qtf /usr/share/icons/hicolor 2>/dev/null || :

%postun
command -v update-desktop-database >/dev/null 2>&1 && update-desktop-database -q /usr/share/applications || :
command -v gtk-update-icon-cache >/dev/null 2>&1 && gtk-update-icon-cache -qtf /usr/share/icons/hicolor 2>/dev/null || :
'
  fi
  cat > "$spec" <<EOF
Name:           $rname
Version:        $(echo "$PKGVER" | tr '-' '.')
Release:        1
Summary:        ${DESC:-Converted package}
License:        Unknown
AutoReqProv:    no

%description
${DESC:-Converted with pkgconvert.}

%install
mkdir -p %{buildroot}
cp -a $STAGE/. %{buildroot}/

%files
/*
$POSTSECTION
EOF
  rpmbuild --define "_topdir $top" --target "$PKGARCH" -bb "$spec" >/dev/null 2>&1 \
    || rpmbuild --define "_topdir $top" -bb "$spec" >/dev/null
  local built
  built="$(find "$top/RPMS" -name '*.rpm' | head -n1)"
  [ -n "$built" ] || err -c 5 "rpmbuild produced no package"
  local out="${OUTBASE}.rpm"
  cp "$built" "$out"
  echo "Created: $out"
  case "$TARGET_DISTRO" in
    opensuse) echo "  Install with: sudo zypper install ./$out" ;;
    *)        echo "  Install with: sudo dnf install ./$out" ;;
  esac
}

case "$OUTFMT" in
  tar.gz|tgz) build_targz ;;
  deb)        build_deb ;;
  rpm)        build_rpm ;;
  dir)        build_dir ;;
  *) err -c 2 "Unknown output format: $OUTFMT (use tar.gz, deb, rpm, or dir)" ;;
esac
